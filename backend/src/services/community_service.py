async def get_post_comments(
    session: AsyncSession,
    post_id: int,
) -> List[PostComments]:
    """Returns ALL comments (top-level + replies) for a post, oldest first.
    Caller is responsible for nesting replies under their parent."""
    result = await session.execute(
        select(PostComments)
        .where(
            and_(
                PostComments.post_id == post_id,
                PostComments.is_deleted == False,
            )
        )
        .order_by(PostComments.created_at.asc())
    )
    return list(result.scalars().all())


async def add_comment(
    session: AsyncSession,
    post_id: int,
    user_id: str,
    text: str,
    parent_comment_id: Optional[int] = None,
) -> PostComments:
    post = await get_post_by_id(session, post_id)
    if not post:
        raise ValueError("Post not found")

    if parent_comment_id:
        parent_result = await session.execute(
            select(PostComments).where(PostComments.id == parent_comment_id)
        )
        parent = parent_result.scalars().first()
        if not parent or parent.post_id != post_id:
            raise ValueError("Parent comment not found")

    comment = PostComments(
        post_id=post_id,
        user_id=user_id,
        parent_comment_id=parent_comment_id,
        text=text,
    )
    session.add(comment)
    post.comment_count += 1

    await session.flush()
    return comment


async def delete_comment(
    session: AsyncSession,
    comment_id: int,
) -> bool:
    result = await session.execute(
        select(PostComments).where(PostComments.id == comment_id)
    )
    comment = result.scalars().first()
    if not comment or comment.is_deleted:
        return False

    comment.is_deleted = True

    post = await get_post_by_id(session, comment.post_id)
    if post:
        post.comment_count = max(0, post.comment_count - 1)

    await session.flush()
    return True


