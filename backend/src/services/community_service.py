async def can_user_view_post(
    session: AsyncSession,
    user_id: str,
    post_id: int
) -> bool:
    post = await get_post_by_id(session, post_id)
    if not post or post.is_deleted:
        return False

    # Posts not tied to a group are visible to anyone (public feed)
    if post.group_id is None:
        return True

    return await is_user_in_group(session, user_id, post.group_id)


async def can_user_modify_post(
    session: AsyncSession,
    user_id: str,
    post_id: int
) -> bool:
    post = await get_post_by_id(session, post_id)
    if not post or post.is_deleted:
        return False

    return post.user_id == user_id

