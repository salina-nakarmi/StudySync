async def create_post(
    session: AsyncSession,
    user_id: str,
    post_type: PostType,
    title: str,
    text: Optional[str] = None,
    group_id: Optional[int] = None,
    resource_id: Optional[int] = None,
    link_data: Optional[dict] = None,
) -> Posts:
    type_data = json.dumps(link_data) if link_data else None

    post = Posts(
        user_id=user_id,
        group_id=group_id,
        post_type=post_type,
        title=title,
        text=text,
        resource_id=resource_id,
        type_data=type_data,
        is_deleted=False,
    )
    session.add(post)
    await session.flush()  # get autogenerated id

    # Notify group members, same pattern as create_resource
    if group_id is not None:
        members = await get_group_members(session, group_id)
        author = await session.get(Users, user_id)
        author_name = author.username if author else "Someone"

        type_label = {
            PostType.RESOURCE: "resource",
            PostType.QUESTION: "question",
            PostType.LINK: "link",
            PostType.ASSIGNMENT: "assignment",
        }.get(post_type, "post")

        for member in members:
            if member[0].user_id != user_id:
                await create_notification(
                    CreateNotificationRequest(
                        user_id=member[0].user_id,
                        title="New Community Post",
                        message=f"{author_name} posted a new {type_label} in your group: \"{title}\"",
                        type="CommunityPostCreated",
                    ),
                    session,
                )
        print(f"Post created and notifications sent for group {group_id}")
    return post


